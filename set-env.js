/**
 * set-env.js
 *
 * Reads environment variables from the CI/CD host (Vercel, Netlify, etc.)
 * and writes them into the Angular environment files before the build runs.
 *
 * Build command on Vercel/Netlify:
 *   node set-env.js && npm run build
 *
 * Required environment variables on your hosting platform:
 *   OPENROUTER_API_KEY
 *   SPOTIFY_CLIENT_ID
 *   SPOTIFY_CLIENT_SECRET
 */

const fs = require('fs');
const path = require('path');

const envDir = path.join(__dirname, 'src', 'environments');

// Ensure target directory exists
if (!fs.existsSync(envDir)) {
  fs.mkdirSync(envDir, { recursive: true });
}

// Read from CI/CD host environment variables
const openRouterApiKey    = process.env.OPENROUTER_API_KEY    || '';
const spotifyClientId     = process.env.SPOTIFY_CLIENT_ID     || '';
const spotifyClientSecret = process.env.SPOTIFY_CLIENT_SECRET || '';

if (!openRouterApiKey)    console.warn('[set-env] Warning: OPENROUTER_API_KEY is not set — AI fallback will be used.');
if (!spotifyClientId)     console.warn('[set-env] Warning: SPOTIFY_CLIENT_ID is not set — mock catalog will be used.');
if (!spotifyClientSecret) console.warn('[set-env] Warning: SPOTIFY_CLIENT_SECRET is not set — mock catalog will be used.');

const sharedConfig = `
  openRouterApiKey: '${openRouterApiKey}',
  openRouterBaseUrl: 'https://openrouter.ai/api/v1',
  openRouterModel: 'openai/gpt-4o-mini',
  spotifyClientId: '${spotifyClientId}',
  spotifyClientSecret: '${spotifyClientSecret}',
  spotifyBaseUrl: 'https://api.spotify.com/v1',
  spotifyAuthUrl: 'https://accounts.spotify.com/api/token',
  appName: 'Spotify AI Discovery',
  appVersion: '1.0.0',
`;

const prodConfig = `// Auto-generated by set-env.js — DO NOT EDIT MANUALLY
// This file is gitignored. Modify environment.template.ts for structure changes.
export const environment = {
  production: true,
${sharedConfig}};
`;

const devConfig = `// Auto-generated by set-env.js — DO NOT EDIT MANUALLY
// This file is gitignored. Modify environment.development.template.ts for structure changes.
export const environment = {
  production: false,
${sharedConfig}};
`;

const prodPath = path.join(envDir, 'environment.ts');
const devPath  = path.join(envDir, 'environment.development.ts');

fs.writeFileSync(prodPath, prodConfig);
console.log(`[set-env] Written: ${prodPath}`);

fs.writeFileSync(devPath, devConfig);
console.log(`[set-env] Written: ${devPath}`);

console.log('[set-env] Environment files generated successfully.');
